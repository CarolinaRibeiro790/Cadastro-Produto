    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        int codigo = Integer.parseInt(txtCodigo.getText());
        String nome = txtNome.getText();
        String descricao = txtDescricao.getText();
        float preco = Float.parseFloat(txtPreco.getText());
        float quantidade = Float.parseFloat(txtQtd.getText());
 
        SupermercadoController supermercadoController = new SupermercadoController();

        if (supermercadoController.cadastrar(codigo, nome, descricao, preco, quantidade, preco * quantidade)) {
            JOptionPane.showMessageDialog(null, "Produto cadastrado com sucesso", "Sucesso", JOptionPane.INFORMATION_MESSAGE);

        } else {
            JOptionPane.showMessageDialog(null, "Erro ao cadastrar o produto", "Erro", JOptionPane.ERROR_MESSAGE);
        }

        // Fecha a tela atual e abre o menu
        this.dispose();
        MenuView menu = new MenuView();
        menu.setVisible(true);
    }//GEN-LAST:event_btnCadastrarActionPerformed

